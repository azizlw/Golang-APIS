	http.HandleFunc("/login", Login)
	http.HandleFunc("/signup", SignUp) // not implemented yet
	http.HandleFunc("/home", Home)
	http.HandleFunc("/refresh", Refresh)

	log.Fatal(http.ListenAndServe(":8080", nil))

